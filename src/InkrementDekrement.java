
public class InkrementDekrement {

	public static void main(String[] args) {
		//Postfix
		//Inkrement / Dekrement VOR der Variable bedeutet ERHÃ–HUNG bzw. VERRINGERUNG VOR der Zuweisung (oder weiterer Rechnung)
		int a = 8;
		int b = ++a;
		
		System.out.println(a);
		System.out.println(b);
		
		int c = 8;
		int d = c++;
		
		System.out.println(c);
		System.out.println(d);

	}

}
